package robin.p.ch4;

public class OperatorEx26 {
	// Class room
	// com.robin.education.english
	public static void main(String[] args) {
		int x=3;
		int y=5;
		
		System.out.println("x is" + x + "and, y is" + y);
		System.out.println("x | y = " + (x|y));
		System.out.println("x & y = " + (x&y));
		System.out.println("x ^ y = " + (x^y));
		
		// x|y, x||y
		
		System.out.println("true|false=" + (true|false));
		System.out.println("true&false=" + (true&false));
		System.out.println("true^false=" + (true^false));
		
		
	}
}
