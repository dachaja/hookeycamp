package anna.p.exercise;

public class XorOperator {
	public static void main(String[] args) {
		int i = 10; // primitive data type, initial, assign 10 to i
		boolean b; // primitive data type, declaration.
		b = true ^ true;
		System.out.println(b);
		b = true ^ false;
		System.out.println(b);
		b = false ^ true;
		System.out.println(b);
		b = false ^ false;
		System.out.println(b);
	}
}
